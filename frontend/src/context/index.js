import { createContext, useContext, useReducer, useMemo, useState, useEffect } from "react";
import { useNavigate } from 'react-router-dom'
import jwt_decode from "jwt-decode";

// prop-types is a library for typechecking of props
import PropTypes from "prop-types";
import input from "assets/theme/components/form/input";

// Material Dashboard 2 React main context
const MaterialUI = createContext();

// Setting custom name for the context which is visible on react dev tools
MaterialUI.displayName = "MaterialUIContext";

// Material Dashboard 2 React reducer
function reducer(state, action) {
  switch (action.type) {
    case "MINI_SIDENAV": {
      return { ...state, miniSidenav: action.value };
    }
    case "TRANSPARENT_SIDENAV": {
      return { ...state, transparentSidenav: action.value };
    }
    case "WHITE_SIDENAV": {
      return { ...state, whiteSidenav: action.value };
    }
    case "SIDENAV_COLOR": {
      return { ...state, sidenavColor: action.value };
    }
    case "TRANSPARENT_NAVBAR": {
      return { ...state, transparentNavbar: action.value };
    }
    case "FIXED_NAVBAR": {
      return { ...state, fixedNavbar: action.value };
    }
    case "OPEN_CONFIGURATOR": {
      return { ...state, openConfigurator: action.value };
    }
    case "DIRECTION": {
      return { ...state, direction: action.value };
    }
    case "LAYOUT": {
      return { ...state, layout: action.value };
    }
    case "DARKMODE": {
      return { ...state, darkMode: action.value };
    }
    case 'SET_TOKEN': {
      return { ...state, token: action.value };
    }
    case 'SET_FLIGHT': {
      return { ...state, flight: action.value };
    }
    default: {
      throw new Error(`Unhandled action type: ${action.type}`);
    }
  }
}

// Material Dashboard 2 React context provider
function MaterialUIControllerProvider({ children }) {
  const initialState = {
    miniSidenav: false,
    transparentSidenav: false,
    whiteSidenav: false,
    sidenavColor: "info",
    transparentNavbar: true,
    fixedNavbar: true,
    openConfigurator: false,
    direction: "ltr",
    layout: "dashboard",
    darkMode: false,
    token: null, // Add token as part of the initial state
    flight: null,
  };

  const [controller, dispatch] = useReducer(reducer, initialState);
  const [token, setToken] = useState(localStorage.getItem("token")); // Add token state
  const [flight, setFlight] = useState(null); // Add flight state

  const value = useMemo(() => [controller, dispatch, token, setToken, flight, setFlight], [controller, dispatch, token, setToken, flight, setFlight]);

  const navigate = useNavigate();
  useEffect(() => {
    if (token) {
      // Token is present at the localstorage
      try {
        var decoded = jwt_decode(token);
        const currentTime = Date.now() / 1000;
        let expired = decoded.exp < currentTime;
        if (expired) {
          setToken(null);
          // Token is expired
          navigate('/authentication/sign-in');
        } else {
          // Token is valid
          let lastLocation = localStorage.getItem("lastLocation");
          if (lastLocation) {            
            navigate(lastLocation);
          } else {           
            navigate('/dashboard');
          }         
        }
      } catch (err) {
        setToken(null);
        // Token is invalid
        navigate('/authentication/sign-in');
      }
    } else {
      // Token is not present in the localstorage
      navigate('/authentication/sign-in');
    }
  }, [])

  return <MaterialUI.Provider value={value}>{children}</MaterialUI.Provider>;
}

// Material Dashboard 2 React custom hook for using context
function useMaterialUIController() {
  const context = useContext(MaterialUI);

  if (!context) {
    throw new Error(
      "useMaterialUIController should be used inside the MaterialUIControllerProvider."
    );
  }

  return context;
}

// Typechecking props for the MaterialUIControllerProvider
MaterialUIControllerProvider.propTypes = {
  children: PropTypes.node.isRequired,
};

// Context module functions
const setMiniSidenav = (dispatch, value) => dispatch({ type: "MINI_SIDENAV", value });
const setTransparentSidenav = (dispatch, value) => dispatch({ type: "TRANSPARENT_SIDENAV", value });
const setWhiteSidenav = (dispatch, value) => dispatch({ type: "WHITE_SIDENAV", value });
